library ieee;
use ieee.std_logic_1164.all;


entity and_gate is

  port (
        in_and1 : in std_logic;
        in_and2 : in std_logic;
        output_and : out std_logic
      );

end and_gate;


architecture behaviour of and_gate is

begin

  output_and <= in_and1 and in_and2;
  
end;
